
 // Classe ForCounter imprime todos os numeros
 // no intervalo de 1 a 10.
 public class ForCounter {
 
   public static void main(String[] args){
   
   // Cabeçalho da instrução for inclui inicialização,
   // Condição de continuação do loop e incremento
	for (int counter = 1 ; counter <= 10 ; counter++)
	System.out.printf(" %d ", counter);
	
	System.out.println(); // gera a saida de um caracter de nova linha
	} // fim de main
} // fim da classe forCounter